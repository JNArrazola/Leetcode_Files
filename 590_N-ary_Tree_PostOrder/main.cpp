// Definition for a Node.
class Node {
public:
    int val;
    vector<Node*> children;

    Node() {}

    Node(int _val) {
        val = _val;
    }

    Node(int _val, vector<Node*> _children) {
        val = _val;
        children = _children;
    }
};


class Solution {
private:
    void postOrder(Node* node, vector<int> &answ){
        vector<Node*> ch = node->children;
        for(Node* n : ch)
            postOrder(n, answ);
        answ.push_back(node->val);
    }
public:
    vector<int> postorder(Node* root) {
        if(!root)  
            return {};
        
        vector<int> answ;
        postOrder(root, answ);
        return answ;
    }
};

