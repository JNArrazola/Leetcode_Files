// Definition for a Node.
class Node {
public:
    int val;
    vector<Node*> children;

    Node() {}

    Node(int _val) {
        val = _val;
    }

    Node(int _val, vector<Node*> _children) {
        val = _val;
        children = _children;
    }
};


class Solution {
public:
    vector<int> preorder(Node* root) {
        if(!root)
            return {};
        
        vector<int> answ;
        stack<Node*> dfs;
        dfs.push(root);

        while (!dfs.empty())
        {
            Node* node = dfs.top();
            dfs.pop();

            answ.push_back(node->val);

            vector<Node*> nodes = node->children;

            for (int i = nodes.size() - 1; i >= 0; i--)
                dfs.push(nodes[i]);
        }
        return answ;
    }
};

